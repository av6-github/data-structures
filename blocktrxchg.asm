.MODEL SMALL
.STACK 100H

.DATA
    SRC  DB 10H, 20H, 30H, 40H, 50H
    DEST DB 5 DUP(?)

.CODE
MAIN PROC
    MOV AX, @DATA
    MOV DS, AX

    LEA SI, SRC
    LEA DI, DEST
    MOV CX, 5

COPY_LOOP:
    MOV AL, [SI]
    XCHG AL, [DI]    ; AL gets garbage, DI gets SRC byte
    INC SI
    INC DI
    LOOP COPY_LOOP

    MOV AH, 4CH
    INT 21H
MAIN ENDP
END MAIN
