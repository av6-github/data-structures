.MODEL SMALL
.STACK 100H

.DATA
MSG1 DB 'HELLO$',0
MSG2 DB 'HELLO$',0
SUCC DB 'EQUAL$'
FAIL DB 'UNEQUAL$'


.CODE
MAIN PROC
	MOV AX, @DATA
	MOV DS, AX
	
	MOV CX, 00H
	
	LEA SI, MSG1
	LEA DI, MSG2
COMP_LOOP:
	MOV AL, [SI]
	MOV BL, [DI]
	CMP AL, BL
	JNE UNEQUAL
	CMP AL, '$'
	JE EQUAL
	INC SI
	INC DI
	JMP COMP_LOOP
EQUAL:
	INC CX
	LEA DX, SUCC
	MOV AH, 09H
	INT 21H
	JMP ENDD
UNEQUAL:
	LEA DX, FAIL
	MOV AH, 09H
	INT 21H
ENDD:
	INT 3
	MOV AH, 4CH
	INT 21H
MAIN ENDP
END MAIN
